function toggleSubMenuInfo(id) {
    var submenuInfo = document.getElementById(id);
    if (submenuInfo.style.display === 'none') {
        submenuInfo.style.display = 'block';
    } else {
        submenuInfo.style.display = 'none';
    }
}

